#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"

/**
  * op_add - adittion
  * @a: first num
  * @b: second num
  * Return: result.
  */
int op_add(int a, int b)
{
return (a + b);
}

/**
  * op_sub - substitution
  * @a: first num
  * @b: second num
  * Return: result.
  */
int op_sub(int a, int b)
{
return (a - b);
}

/**
  * op_mul - multiplication
  * @a: first num
  * @b: second num
  * Return: result.
  */
int op_mul(int a, int b)
{
return (a * b);
}

/**
  * op_div - division
  * @a: first num
  * @b: second num
  * Return: result.
  */
int op_div(int a, int b)
{
return (a / b);
}

/**
  * op_mod - modulo
  * @a: first num
  * @b: second num
  * Return: result.
  */
int op_mod(int a, int b)
{
return (a % b);
}
